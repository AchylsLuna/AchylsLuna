    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     JPanel settingsPanel = new JPanel();
    
    // Create a checkbox for music on/off
    JCheckBox musicCheckBox = new JCheckBox("Music On/Off");
    settingsPanel.add(musicCheckBox);
    
    // Create a checkbox for time option
    JCheckBox timeCheckBox = new JCheckBox("Enable Time Option");
    settingsPanel.add(timeCheckBox);
    
    // Show the settings dialog
    int result = JOptionPane.showConfirmDialog(this, settingsPanel, "Settings", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
    
    // Check the result
    if (result == JOptionPane.OK_OPTION) {
        // Check if music checkbox is selected
        if (musicCheckBox.isSelected()) {
            // Code to turn on music
            JOptionPane.showMessageDialog(this, "Music turned on", "Music", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Code to turn off music
            JOptionPane.showMessageDialog(this, "Music turned off", "Music", JOptionPane.INFORMATION_MESSAGE);
        }
        
        // Check if time checkbox is selected
        if (timeCheckBox.isSelected()) {
            // Code to enable time option
            JOptionPane.showMessageDialog(this, "Time option enabled", "Time Option", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Code to disable time option
            JOptionPane.showMessageDialog(this, "Time option disabled", "Time Option", JOptionPane.INFORMATION_MESSAGE);
        }
    }
   
    }//GEN-LAST:event_jButton2ActionPerformed

